package com.example.library.enums;

/**
 * Represents the role of a user in the system.
 * The role is an enumeration with two possible values.
 */
public enum Role {

    /**
     * Represents a user who is a reader.
     */
    ROLE_READER,

    /**
     * Represents a user who is a staff member.
     */
    ROLE_STAFF
}

