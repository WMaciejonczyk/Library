package com.example.library.enums;

/**
 * Represents a rating for a book.
 * The rating is an enumeration with five possible values representing stars.
 */
public enum Rating {

    /**
     * Represents a rating of one star.
     */
    ONE,

    /**
     * Represents a rating of two stars.
     */
    TWO,

    /**
     * Represents a rating of three stars.
     */
    THREE,

    /**
     * Represents a rating of four stars.
     */
    FOUR,

    /**
     * Represents a rating of five stars.
     */
    FIVE
}

